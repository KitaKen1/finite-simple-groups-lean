elations = f"""/-
Copyright (c) 2026 Kenta. All rights reserved.
Released under Apache 2.0 license as described in the file LICENSE.
Authors: Kenta, Claude
-/
import SporadicM22.L34.Transitivity

/-!
# Elations of PG(2,4) and their equivariance

For each point `x` of the 21-point plane, `elationAt x k` (`k : Fin 15`)
enumerates the fifteen nontrivial elations with center `x` — involutions
moving 16 points in eight transpositions. Certificates:

* commutativity of the fifteen elations at the base point `0`;
* conjugation tables for the three generators
  (`g * elationAt x k * g = elationAt (g x) (conjIdxI x k)`);
* membership words for the elations at `0`;
* commutator identities expressing each generator as `⁅u,v⁆` of elations.

From the tables, closure induction gives full equivariance: every element
of `L34Subgroup` permutes the elation family. This is the engine of the
Iwasawa-style simplicity proof.
-/

namespace Sporadic
namespace L34

/-- Transposition data for each elation. -/
def elationPairs (x : Fin 21) (k : Fin 15) : List (Fin 21 × Fin 21) :=
  match x.val, k.val with
{el_table}
  | _, _ => []

/-- The elation with center `x` and index `k`, as a product of swaps. -/
def elationAt (x : Fin 21) (k : Fin 15) : Perm21 :=
  ((elationPairs x k).map fun p => Equiv.swap p.1 p.2).prod

set_option maxRecDepth 200000 in
set_option maxHeartbeats 1600000 in
-- The involution certificate is intentionally checked by computation.
/-- Every elation is an involution. -/
theorem elation_sq_pointwise :
    ∀ x : Fin 21, ∀ k : Fin 15, ∀ p : Fin 21,
      (elationAt x k * elationAt x k) p = p := by
  decide +kernel

theorem elation_sq (x : Fin 21) (k : Fin 15) :
    elationAt x k * elationAt x k = 1 :=
  Equiv.ext fun p => elation_sq_pointwise x k p

theorem elation_inv (x : Fin 21) (k : Fin 15) :
    (elationAt x k)⁻¹ = elationAt x k :=
  inv_eq_of_mul_eq_one_right (elation_sq x k)

set_option maxRecDepth 200000 in
set_option maxHeartbeats 1600000 in
-- The base-point commutativity certificate is intentionally checked by computation.
/-- The fifteen elations at the base point `0` commute pairwise. -/
theorem elation_comm_zero_pointwise :
    ∀ k k' : Fin 15, ∀ p : Fin 21,
      (elationAt 0 k * elationAt 0 k') p = (elationAt 0 k' * elationAt 0 k) p := by
  decide +kernel

theorem elation_comm_zero (k k' : Fin 15) :
    elationAt 0 k * elationAt 0 k' = elationAt 0 k' * elationAt 0 k :=
  Equiv.ext fun p => elation_comm_zero_pointwise k k' p

{chr(10).join(conj_defs)}
set_option maxRecDepth 200000 in
set_option maxHeartbeats 6400000 in
-- The conjugation certificate is intentionally checked by computation.
/-- Conjugation by generator 1 permutes the elation family. -/
theorem conj1_pointwise :
    ∀ x : Fin 21, ∀ k : Fin 15, ∀ p : Fin 21,
      (l34e1 * elationAt x k * l34e1) p =
        (elationAt (l34e1 x) (conjIdx1 x k)) p := by
  decide +kernel

set_option maxRecDepth 200000 in
set_option maxHeartbeats 6400000 in
-- The conjugation certificate is intentionally checked by computation.
/-- Conjugation by generator 2 permutes the elation family. -/
theorem conj2_pointwise :
    ∀ x : Fin 21, ∀ k : Fin 15, ∀ p : Fin 21,
      (l34e2 * elationAt x k * l34e2) p =
        (elationAt (l34e2 x) (conjIdx2 x k)) p := by
  decide +kernel

set_option maxRecDepth 200000 in
set_option maxHeartbeats 6400000 in
-- The conjugation certificate is intentionally checked by computation.
/-- Conjugation by generator 3 permutes the elation family. -/
theorem conj3_pointwise :
    ∀ x : Fin 21, ∀ k : Fin 15, ∀ p : Fin 21,
      (l34e3 * elationAt x k * l34e3) p =
        (elationAt (l34e3 x) (conjIdx3 x k)) p := by
  decide +kernel

theorem conj1_eq (x : Fin 21) (k : Fin 15) :
    l34e1 * elationAt x k * l34e1⁻¹ = elationAt (l34e1 x) (conjIdx1 x k) := by
  rw [l34e1_inv]
  exact Equiv.ext fun p => conj1_pointwise x k p

theorem conj2_eq (x : Fin 21) (k : Fin 15) :
    l34e2 * elationAt x k * l34e2⁻¹ = elationAt (l34e2 x) (conjIdx2 x k) := by
  rw [l34e2_inv]
  exact Equiv.ext fun p => conj2_pointwise x k p

theorem conj3_eq (x : Fin 21) (k : Fin 15) :
    l34e3 * elationAt x k * l34e3⁻¹ = elationAt (l34e3 x) (conjIdx3 x k) := by
  rw [l34e3_inv]
  exact Equiv.ext fun p => conj3_pointwise x k p

/-- A permutation conjugates every elation to an elation at the image center. -/
def MapsElations (g : Perm21) : Prop :=
  ∀ x : Fin 21, ∀ k : Fin 15, ∃ k' : Fin 15,
    g * elationAt x k * g⁻¹ = elationAt (g x) k'

theorem mapsElations_one : MapsElations 1 := fun x k =>
  ⟨k, by simp⟩

theorem mapsElations_mul {{g h : Perm21}}
    (hg : MapsElations g) (hh : MapsElations h) : MapsElations (g * h) := by
  intro x k
  obtain ⟨k₁, hk₁⟩ := hh x k
  obtain ⟨k₂, hk₂⟩ := hg (h x) k₁
  refine ⟨k₂, ?_⟩
  have : g * h * elationAt x k * (g * h)⁻¹ =
      g * (h * elationAt x k * h⁻¹) * g⁻¹ := by
    group
  rw [this, hk₁, hk₂]
  rfl

/-- Two-sided predicate, closed under the closure operations. -/
def MapsElations₂ (g : Perm21) : Prop :=
  MapsElations g ∧ MapsElations g⁻¹

theorem mapsElations₂_gen1 : MapsElations₂ l34e1 := by
  constructor
  · exact fun x k => ⟨conjIdx1 x k, conj1_eq x k⟩
  · rw [l34e1_inv]
    exact fun x k => ⟨conjIdx1 x k, conj1_eq x k⟩

theorem mapsElations₂_gen2 : MapsElations₂ l34e2 := by
  constructor
  · exact fun x k => ⟨conjIdx2 x k, conj2_eq x k⟩
  · rw [l34e2_inv]
    exact fun x k => ⟨conjIdx2 x k, conj2_eq x k⟩

theorem mapsElations₂_gen3 : MapsElations₂ l34e3 := by
  constructor
  · exact fun x k => ⟨conjIdx3 x k, conj3_eq x k⟩
  · rw [l34e3_inv]
    exact fun x k => ⟨conjIdx3 x k, conj3_eq x k⟩

theorem mapsElations₂_one : MapsElations₂ 1 := by
  constructor
  · exact mapsElations_one
  · simpa using mapsElations_one

theorem mapsElations₂_mul {{g h : Perm21}}
    (hg : MapsElations₂ g) (hh : MapsElations₂ h) : MapsElations₂ (g * h) := by
  constructor
  · exact mapsElations_mul hg.1 hh.1
  · rw [mul_inv_rev]
    exact mapsElations_mul hh.2 hg.2

theorem mapsElations₂_inv {{g : Perm21}}
    (hg : MapsElations₂ g) : MapsElations₂ g⁻¹ := by
  constructor
  · exact hg.2
  · rw [inv_inv]
    exact hg.1

/-- Every element of `L34Subgroup` permutes the elation family (both ways). -/
theorem mapsElations₂_of_mem {{g : Perm21}} (hg : g ∈ L34Subgroup) :
    MapsElations₂ g := by
  exact Subgroup.closure_induction
    (p := fun x _ => MapsElations₂ x)
    (fun x hx => by
      simp only [Set.mem_insert_iff, Set.mem_singleton_iff] at hx
      rcases hx with rfl | rfl | rfl
      · exact mapsElations₂_gen1
      · exact mapsElations₂_gen2
      · exact mapsElations₂_gen3)
    mapsElations₂_one
    (fun x y _ _ hx hy => mapsElations₂_mul hx hy)
    (fun x _ hx => mapsElations₂_inv hx)
    hg

/-- Words at the base point: each elation at `0` as a generator word. -/
def elationZeroWord (k : Fin 15) : Word :=
  match k.val with
{chr(10).join(el0_lines)}
  | _ => []

set_option maxRecDepth 400000 in
set_option maxHeartbeats 3200000 in
-- The base-membership certificate is intentionally checked by computation.
/-- The base-point words evaluate to the elations at `0`. -/
theorem elationZeroWord_eval_pointwise :
    ∀ k : Fin 15, ∀ p : Fin 21,
      (Word.eval (elationZeroWord k)) p = (elationAt 0 k) p := by
  decide +kernel

theorem elationZeroWord_eval (k : Fin 15) :
    Word.eval (elationZeroWord k) = elationAt 0 k :=
  Equiv.ext fun p => elationZeroWord_eval_pointwise k p

/-- The elations at the base point belong to the group. -/
theorem elation_zero_mem (k : Fin 15) : elationAt 0 k ∈ L34Subgroup := by
  rw [← elationZeroWord_eval]
  exact Word.eval_mem_L34Subgroup _

/-- Every elation belongs to the group. -/
theorem elation_mem (x : Fin 21) (k : Fin 15) : elationAt x k ∈ L34Subgroup := by
  have hw : (Word.eval (orbitZeroWord x)) (0 : Fin 21) = x :=
    orbitZeroWord_maps_zero_all x
  set g : Perm21 := Word.eval (orbitZeroWord x) with hgdef
  have hgmem : g ∈ L34Subgroup := Word.eval_mem_L34Subgroup _
  have hginv : g⁻¹ x = 0 := by
    rw [← hw]
    exact Equiv.Perm.inv_eq_iff_eq.mpr rfl
  obtain ⟨k', hk'⟩ := (mapsElations₂_of_mem hgmem).2 x k
  rw [hginv] at hk'
  have : elationAt x k = g * elationAt 0 k' * g⁻¹ := by
    rw [← hk']
    group
  rw [this]
  exact Subgroup.mul_mem _ (Subgroup.mul_mem _ hgmem (elation_zero_mem k'))
    (Subgroup.inv_mem _ hgmem)

{chr(10).join(comm_text)}
end L34
end Sporadic
"""
open("../SporadicM22/L34/Elations.lean", "w").write(elations)
