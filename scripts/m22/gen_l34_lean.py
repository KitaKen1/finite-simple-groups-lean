LEAN3 = {"1": "Gen.e1", "2": "Gen.e2", "3": "Gen.e3"}
LEAN22 = {"a": "Gen.a", "A": "Gen.aInv", "b": "Gen.b", "B": "Gen.bInv"}

def word3(w):
    if not w:
        return "[]"
    return "[" + ", ".join(LEAN3[c] for c in w) + "]"

def word22(w):
    if not w:
        return "[]"
    return "[" + ", ".join(LEAN22[c] for c in w) + "]"

def match_table(tbl, fmt):
    lines = []
    for x in sorted(int(k) for k in tbl):
        lines.append(f"  | {x} => {fmt(tbl[str(x)])}")
    lines.append("  | _ => []")
    return "\n".join(lines)

def cyc_prod(cycles, n):
    parts = [f"List.formPerm ([{', '.join(map(str, c))}] : List (Fin {n}))"
             for c in cycles]
    return " *\n    ".join(parts)

